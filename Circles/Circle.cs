namespace Circles
{
    public struct Circle
    {
        public double xPos;
        public double yPos;
        public double xDir;
        public double yDir;

        public Circle(double _xPos, double _yPos, double _xDir, double _yDir)
        {
            xPos = _xPos;
            yPos = _yPos;
            xDir = _xDir;
            yDir = _yDir;
        }
    }
}


