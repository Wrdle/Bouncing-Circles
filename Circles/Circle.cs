namespace Circles
{
    public struct Circle
    {
        public int xPos;
        public int yPos;
        public int xDir;
        public int yDir;

        public Circle(int _xPos, int _yPos, int _xDir, int _yDir)
        {
            xPos = _xPos;
            yPos = _yPos;
            xDir = _xDir;
            yDir = _yDir;
        }
    }
}


